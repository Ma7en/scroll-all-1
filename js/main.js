console.log(window.innerHeight  );

let s1Boxes = document.querySelectorAll(".scroll-1 .box");

window.addEventListener("scroll", checkBox);

checkBox();

function checkBox() {
    let s1TriggerPoint = window.innerHeight / 5 * 4 ;

    s1Boxes.forEach(box => {
        // console.log(box.getBoundingClientRect().top);
        
        let s1TopPoint = box.getBoundingClientRect().top ;
    
        if(s1TopPoint < s1TriggerPoint) {
            box.classList.add("show");
        } else {
            box.classList.remove("show");
        }
    
    });

}



